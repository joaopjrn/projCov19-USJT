  private void btnCadPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadPacienteActionPerformed
    new Thread(() -> {
      tp.carregando();
      tudoDesabilitadoCad();
      
      String cpf = campoCpfCad.getText();
      String nome = campoNomeCad.getText();
//      LocalDate dataNasc = Instant.ofEpochMilli(campoDataNasc.getDate().getTime()).atZone(ZoneId.systemDefault()).toLocalDate();
      Date dataNasc = campoDataNasc.getDate();
      boolean isSaude = campoSaudeCad.getSelectedIndex() == 1 ? true : false;
      String cep = null;
      cep = sabeCep ? campoCepCad.getText() : "";
      String rua = campoRuaCad.getText();
      String num = campoNumCad.getText();
      String comp = campoComplemento.getText();
      String bairro = campoBairro.getText();
      String cidade = campoCidade.getText();
      String uf = campoEstado.getText();
      
      int cod = usuarioLogado.cadPaciente(cpf, nome, dataNasc, isSaude, cep, rua, num, bairro, comp, cidade, uf);
      
      if(cod <= 0 && !sabeCep){
        estadoNaoSeiCep();
      }else if(cod <= 0 && sabeCep){
        estInicialCad();
        btnCadPaciente.setEnabled(true);
        campoCepCad.setEnabled(true);
      }else if (cod > 0){
        tp.fila = null;
        tp.s = null;
        estInicialCad();
        limparTudoCad();
      }
      
      tp.carregando();
    }).start();
  }//GEN-LAST:event_btnCadPacienteActionPerformed

