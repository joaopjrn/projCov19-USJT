  private void setEstadoCad(boolean b, JComponent[] lista) {
//    setEstado(false, new JComponent[]{campoCepCad, btnCadPaciente});
    List<JComponent> a = Arrays.asList(lista);
    JComponent[] x = {jtbBase, campoCpfCad, campoNomeCad, campoDataNasc, campoSaudeCad, campoCepCad,
      btnBuscarCep, btnNaoSeiCep, campoRuaCad, campoBairro, campoNumCad, campoComplemento, campoCidade, campoEstado, btnCadPaciente, btnLimpar};
    for (JComponent c : x) {
      if (a.contains(c)) {
        c.setEnabled(b);
      } else {
        c.setEnabled(!b);
      }
    }
  }
  
