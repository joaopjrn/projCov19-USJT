  private void carregarFila() {
    new Thread(() -> {
      tp.carregando();
      if(tp.fila == null) tp.fila = usuarioLogado.verFila();
      preencherFila();
      tp.carregando();
      btnPagConfVac.setEnabled(true);
    }).start();
  }
  
  private void preencherFila() {
    DefaultTableModel dtm = (DefaultTableModel) tabelaFila.getModel();
    for (Paciente p : tp.fila) {
      dtm.addRow(p.toTableRow());
    }
  }

