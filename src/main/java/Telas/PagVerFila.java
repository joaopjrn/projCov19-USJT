  private void btnPagConfVacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagConfVacActionPerformed
    if (tabelaFila.getSelectedRow() == -1) {
      JOptionPane.showMessageDialog(null, "Selecione um Paciente da lista por favor!", "Aviso", JOptionPane.WARNING_MESSAGE);
      return;
    }
    if (tabelaFila.getSelectedRow() != 0) {
      int x = JOptionPane.showConfirmDialog(null, "Este não é o primeiro paciente da fila, deseja confirmar a vacina mesmo assim?", "Aviso", JOptionPane.WARNING_MESSAGE);
      if(x == JOptionPane.YES_OPTION){
        tp.trocarPag(new PagConfVac(tp.fila.get(tabelaFila.getSelectedRow()), this.usuarioLogado, this.tp));
      }else{
        return;
      }
    } else {
      tp.trocarPag(new PagConfVac(tp.fila.get(tabelaFila.getSelectedRow()), this.usuarioLogado, this.tp));
    }
  }//GEN-LAST:event_btnPagConfVacActionPerformed


