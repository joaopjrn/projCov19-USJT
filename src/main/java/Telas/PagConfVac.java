  private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
    new Thread(() -> {
      tp.carregando();
      campoBuscaCpf.setEnabled(false);
      btnBusca.setEnabled(false);
      this.pSelecionado = usuarioLogado.buscarParaVac(campoBuscaCpf.getText());
      if (this.pSelecionado != null) {
        pnBase.remove(0);
        pnBase.add(pnInfoPaciente);
        if (pSelecionado.getDataVac() != null) {
          btnConfVac.setEnabled(false);
        }
        revalidate();
        repaint();
        setInfo();
      }
      tp.carregando();
      campoBuscaCpf.setEnabled(true);
      btnBusca.setEnabled(true);
    }).start();
  }//GEN-LAST:event_btnBuscaActionPerformed

  private void btnConfVacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfVacActionPerformed
    new Thread(() -> {
      btnConfVac.setEnabled(false);
      tp.carregando();
      int cod = usuarioLogado.confVac(pSelecionado, usuarioLogado);
      if(cod > 0){
        txtDataVac.setText("Data de Vacinação: " + pSelecionado.getDataVac().format(DateTimeFormatter.ofPattern("d/M/y")));
        txtVacPor.setText("Vacinado por: " + usuarioLogado.getNome());
        tp.fila = null;
        tp.s = null;
      } else {
        btnConfVac.setEnabled(true);
      }
      tp.carregando();
    }).start();
  }//GEN-LAST:event_btnConfVacActionPerformed


