  private void setInfo(){
    String saude = pSelecionado.isSaude() ? "Sim" : "Não";
    String dVac = pSelecionado.getDataVac() == null ? "---" : pSelecionado.getDataVac().toString();
    String vacPor = pSelecionado.getNomeVacPor() == null ? "---" : pSelecionado.getNomeVacPor().toString();
    txtCpf.setText(txtCpf.getText() + pSelecionado.getCpf());
    txtNome.setText(txtNome.getText() + pSelecionado.getNome());
    txtIdade.setText(txtIdade.getText() + pSelecionado.getIdade());
    txtSaude.setText(txtSaude.getText() + saude);
    txtDataVac.setText(txtDataVac.getText() + dVac);
    txtVacPor.setText(txtVacPor.getText() + vacPor);
  }
  
  private void selecionarTela(){
    if (pSelecionado == null){
      pnBase.add(pnBuscaPaciente);
    }else{
      pnBase.add(pnInfoPaciente);
    }
    revalidate();
    repaint();
  }

